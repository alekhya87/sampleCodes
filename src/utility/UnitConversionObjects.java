package utility;

public class UnitConversionObjects {
    private int Feets;
    private int Inchs;
    private int Meters;

    public int getFeets() {
        return Feets;
    }

    public void setFeets(int feets) {
        Feets = feets;
    }

    public int getInchs() {
        return Inchs;
    }

    public void setInchs(int inchs) {
        Inchs = inchs;
    }

    public int getMeters() {
        return Meters;
    }

    public void setMeters(int meters) {
        Meters = meters;
    }
}
